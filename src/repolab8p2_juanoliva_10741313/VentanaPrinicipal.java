    private void jb_AgregarBusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_AgregarBusActionPerformed
        Bus b = new Bus();
        String nombre = jt_PlacaBus_Agregar.getText();
        String marca = jt_MarcaBus_Agregar.getText();
        double velocidad = 0;
        boolean v1 = !nombre.isEmpty();
        boolean v2 = !marca.isEmpty();
        boolean v3 = true;
        String temp = jt_VelocidadBus_Agregar.getText();
        for (int i = 0; i < temp.length(); i++) {
            if (!Character.isDigit(temp.charAt(i))) {
                v3 = false;
                break;
            }
        }
        if (v3) {
            velocidad = Double.valueOf(jt_VelocidadBus_Agregar.getText());
        }
        boolean validacion = v1&v2&v3;
        if (validacion) {
            b.setPlaca(nombre);
            b.setMarca(marca);
            b.setVelocidad(velocidad);
            b.setColor(colorBusAgregar);
            db.getBuses().add(b);
            JOptionPane.showMessageDialog(jd_AgregarBus, "El bus ha sido agregado exitosamente");
        }else{
            JOptionPane.showMessageDialog(jd_AgregarBus, "Revise los datos ingresados");
        }
    }//GEN-LAST:event_jb_AgregarBusActionPerformed

