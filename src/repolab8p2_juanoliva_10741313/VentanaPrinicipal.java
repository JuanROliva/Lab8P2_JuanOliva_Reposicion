    private void selecionarColor(){
        String s = jc_ColorBus_Agregar.getSelectedItem().toString();
        switch (s) {
            case "Amarillo":
                colorBusAgregar= Color.YELLOW;
                break;
            case "Rojo":
                colorBusAgregar = Color.RED;
                break;
            case "Azul":
                colorBusAgregar = Color.BLUE;
                break;
            case "Negro":
                colorBusAgregar = Color.BLACK;
                break;
            default:
                colorBusAgregar = Color.YELLOW;
                break;
        }
    }


